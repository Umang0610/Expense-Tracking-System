    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int ri=table.getSelectedRow();
        if(ri!=-1){
            int id=(int)table.getValueAt(ri, 0);
            int r=JOptionPane.showConfirmDialog(null,"Do you really want to delete the selected Category?","Deletion Confirmation",JOptionPane.YES_NO_OPTION);
            if(r==JOptionPane.YES_NO_OPTION){
                String category=(String)table.getValueAt(ri,1);
            try{
                db.DbConnect.st.executeUpdate("Delete from spendings where S_ID="+id);
                JOptionPane.showMessageDialog(null, "Successfully Deleted!");
                getEntries();
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
            }
            }  
        }
    }//GEN-LAST:event_jButton3ActionPerformed

